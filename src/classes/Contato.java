package classes;

public class Contato {
	public int id_contato;
	public String nome;
	public Integer telefone;
	public int ddd;
	public String email;

	
	public int getId() {
		return id_contato;
	}
	public String getNome() {
		return nome;
	}
	public int getTelefone() {
		return telefone;
	}
	public int getDdd() {
		return ddd;
	}
	public String getEmail() {
		return email;
	}
}
